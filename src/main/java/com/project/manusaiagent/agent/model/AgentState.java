package com.project.manusaiagent.agent.model;

/**
 * An enumeration class of agent execution states
 */
public enum AgentState {

    /**
     * IDLE STATE
     */
    IDLE,

    /**
     * THE RUNNING STATE
     */
    RUNNING,

    /**
     * COMPLETED STATUS
     */
    FINISHED,

    /**
     * ERROR STATUS
     */
    ERROR
}